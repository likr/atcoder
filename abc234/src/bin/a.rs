#[allow(unused_macros)]
macro_rules! debug {
    ($($a:expr),* $(,)*) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($("| ", stringify!($a), "={:?} "),*, "|"), $(&$a),*);
    };
}

fn f(x: usize) -> usize {
    x * x + 2 * x + 3
}

fn main() {
    input! {
        t: usize,
    }
    println!("{}", f(f(f(t) + t) + f(f(t))));
}

fn main() {
    input! {
        n: usize,
        p: [usize; n],
    }
    let mut nums = BTreeSet::new();
    for i in 0..=200001 {
        nums.insert(i);
    }
    for i in 0..n {
        nums.remove(&p[i]);
        println!("{}", nums.iter().nth(0).unwrap());
    }
}
